<h4 class="pageTitle">About</h4>
<h5>Content</h5>
<p>Most of what you see on this site has been collected from other resources.
These are other website services that I use such as <a href="http://twitter.com/hardcastle">Twitter</a>, <a href="http://hardcastle.tumblr.com">Tumblr</a> and <a href="http://github.com/chardcastle">GitHub</a>.&nbsp;If you don't already use these, jump in and check them out!</p>
<h5>Technology</h5>
<p>I've built this site using the Kohana PHP framework. I had originally planned to use Expression Engine,
 the first version of which was great. However I missed the flexibility of a framework and I don't expect
 to be publishing on a scale that requires a content management system.</p>
<h5>Purpose</h5>
<p>I created this site as technical exercise. I've made the <a href="http://github.com/chardcastle">source code available</a> for others to read. I'd really like some feedback, so please feel free to comment. All my work is within the application folder of course. The core is unaltered.</p>
